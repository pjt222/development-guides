#!/usr/bin/env node
/**
 * build-workflow.js
 *
 * Generates a Mermaid flowchart describing the viz build pipeline itself,
 * derived from PUT annotations in the source files.
 *
 * Output: public/data/workflow.mmd
 *
 * PUT:meta-pipeline  [entry] Scan source files for PUT annotations
 * PUT:meta-pipeline  [step]  Group annotations by pipeline name
 * PUT:meta-pipeline  [step]  Generate Mermaid flowchart syntax
 * PUT:meta-pipeline  [exit]  Write public/data/workflow.mmd
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'fs';
import { resolve, dirname, basename, extname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(__dirname, 'public', 'data', 'workflow.mmd');

// ── Files to scan for PUT annotations ───────────────────────────
const SCAN_FILES = [
  'build-data.js',
  'R/render.R',
  'R/glyphs.R',
  'js/app.js',
  'js/graph.js',
  'js/graph3d.js',
  'js/hive.js',
  'js/chord.js',
  'js/workflow.js',
];

// ── Parse PUT annotations from a file ───────────────────────────
function parsePutAnnotations(filePath) {
  if (!existsSync(filePath)) return [];

  const content = readFileSync(filePath, 'utf8');
  const lines = content.split('\n');
  const annotations = [];

  for (let i = 0; i < lines.length; i++) {
    // Match: PUT:<pipeline>  [<type>] <description>
    const match = lines[i].match(/PUT:(\S+)\s+\[(\S+)]\s+(.+)/);
    if (match) {
      annotations.push({
        pipeline: match[1],
        type: match[2],
        description: match[3].trim(),
        file: basename(filePath),
        line: i + 1,
      });
    }
  }

  return annotations;
}

// ── Collect all annotations ─────────────────────────────────────
const allAnnotations = [];
for (const relPath of SCAN_FILES) {
  const fullPath = resolve(__dirname, relPath);
  allAnnotations.push(...parsePutAnnotations(fullPath));
}

// ── Group by pipeline ───────────────────────────────────────────
const pipelines = {};
for (const ann of allAnnotations) {
  if (!pipelines[ann.pipeline]) pipelines[ann.pipeline] = [];
  pipelines[ann.pipeline].push(ann);
}

// ── Generate Mermaid syntax ─────────────────────────────────────
let mermaid = `%% Auto-generated by build-workflow.js — do not edit manually
%% Source: PUT annotations across viz/ codebase
flowchart TD

`;

// Sanitize ID: replace non-alphanumeric with underscore
function sanitizeId(str) {
  return str.replace(/[^a-zA-Z0-9]/g, '_');
}

let nodeCounter = 0;
function nextId(prefix) {
  return `${prefix}_${++nodeCounter}`;
}

// ── Data Pipeline ───────────────────────────────────────────────
mermaid += `  %% ── Data Pipeline ──────────────────────────────\n`;
mermaid += `  subgraph data_pipeline["Data Pipeline (build-data.js)"]\n`;
mermaid += `    direction TB\n`;
mermaid += `    D1[/"skills/_registry.yml\\nagents/_registry.yml\\nteams/_registry.yml"/]\n`;
mermaid += `    D2["Parse skill nodes &\\ncross-reference links"]\n`;
mermaid += `    D3["Parse agent nodes &\\nagent→skill links"]\n`;
mermaid += `    D4["Parse team nodes &\\nteam→agent links"]\n`;
mermaid += `    D5["Merge into unified graph"]\n`;
mermaid += `    D6[("public/data/\\nskills.json")]\n`;
mermaid += `    D1 --> D2 --> D3 --> D4 --> D5 --> D6\n`;
mermaid += `  end\n\n`;

// ── Icon Pipeline ───────────────────────────────────────────────
mermaid += `  %% ── Icon Pipeline ──────────────────────────────\n`;
mermaid += `  subgraph icon_pipeline["Icon Pipeline (R + magick)"]\n`;
mermaid += `    direction TB\n`;
mermaid += `    I1[/"SKILL_GLYPHS mapping\\n(glyphs.R)"/]\n`;
mermaid += `    I2["Resolve glyph function"]\n`;
mermaid += `    I3["Draw glyph layers\\n(ggplot2 100x100 canvas)"]\n`;
mermaid += `    I4["Apply neon glow\\n(ggfx::with_outer_glow)"]\n`;
mermaid += `    I5["Rasterize to PNG\\n(ragg::agg_png 512px)"]\n`;
mermaid += `    I6[("WebP icons\\n(magick::image_write)")]\n`;
mermaid += `    I1 --> I2 --> I3 --> I4 --> I5 --> I6\n`;
mermaid += `  end\n\n`;

// ── Frontend Pipeline ───────────────────────────────────────────
mermaid += `  %% ── Frontend Pipeline ─────────────────────────\n`;
mermaid += `  subgraph frontend_pipeline["Frontend Pipeline (app.js)"]\n`;
mermaid += `    direction TB\n`;
mermaid += `    F1[/"Fetch skills.json"/]\n`;
mermaid += `    F2["Init filters, panels, graph"]\n`;
mermaid += `    F3{"Mode switch"}\n`;
mermaid += `    F4["2D Force\\n(graph.js)"]\n`;
mermaid += `    F5["3D Force\\n(graph3d.js)"]\n`;
mermaid += `    F6["Hive Plot\\n(hive.js)"]\n`;
mermaid += `    F7["Chord Diagram\\n(chord.js)"]\n`;
mermaid += `    F8["Workflow\\n(workflow.js)"]\n`;
mermaid += `    F1 --> F2 --> F3\n`;
mermaid += `    F3 --> F4\n`;
mermaid += `    F3 --> F5\n`;
mermaid += `    F3 --> F6\n`;
mermaid += `    F3 --> F7\n`;
mermaid += `    F3 --> F8\n`;
mermaid += `  end\n\n`;

// ── Cross-pipeline connections ───────────────────────────────────
mermaid += `  %% ── Cross-pipeline connections ─────────────────\n`;
mermaid += `  D6 -.->|"runtime fetch"| F1\n`;
mermaid += `  I6 -.->|"icon assets"| F2\n`;

// ── Styling ─────────────────────────────────────────────────────
mermaid += `\n  %% ── Styling ────────────────────────────────────\n`;
mermaid += `  classDef entry fill:#1a1a2e,stroke:#00ff88,color:#00ff88\n`;
mermaid += `  classDef step fill:#16213e,stroke:#44ddff,color:#44ddff\n`;
mermaid += `  classDef output fill:#0f3460,stroke:#ff3366,color:#ff3366\n`;
mermaid += `  classDef decision fill:#1a1a2e,stroke:#ffaa33,color:#ffaa33\n`;
mermaid += `  class D1,I1,F1 entry\n`;
mermaid += `  class D2,D3,D4,D5,I2,I3,I4,I5,F2 step\n`;
mermaid += `  class D6,I6 output\n`;
mermaid += `  class F3 decision\n`;
mermaid += `  class F4,F5,F6,F7,F8 step\n`;

writeFileSync(OUTPUT_PATH, mermaid);
console.log(`Generated ${OUTPUT_PATH}`);
console.log(`  Pipelines found: ${Object.keys(pipelines).join(', ')}`);
console.log(`  Total annotations: ${allAnnotations.length}`);
